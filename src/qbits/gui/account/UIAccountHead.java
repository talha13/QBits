    private void changeStatus(boolean status) {

        txfAccountHead.setEnabled(status);
        cmbPrinciple.setEnabled(status);
        rbActive.setEnabled(status);
        rbInactive.setEnabled(status);
        btnReset.setEnabled(status);
        btnSave.setEnabled(status);

    }

    private boolean check() {

        if (!Utilities.isValidString(txfAccountHead.getText())) {
            parentFrame.showMessage("Please enter valid account head");
            return false;
        }

        if (cmbPrinciple.getSelectedIndex() == 0) {
            parentFrame.showMessage("Please select account principle");
            return false;
        }

        if (!rbActive.isSelected() && !rbInactive.isSelected()) {
            parentFrame.showMessage("Please select status");
            return false;
        }

        return true;
    }

    private void reset() {

        txfAccountHead.setText("");
        cmbPrinciple.setSelectedIndex(0);
        rbActive.setSelected(true);
    }

    private int save() {

        MySQLDatabase database = new MySQLDatabase();
        String query;
        QueryBuilder builder = new QueryBuilder();

        if (database.connect()) {

//            query = "INSERT INTO account_head VALUES("
//                    + "null, \"" + txfAccountHead.getText().trim() + "\", \"" + cmbPrinciple.getSelectedItem().toString() + "\""
//                    + ", " + rbActive.isSelected() + ""
//                    + ")";

            builder.setString("title", txfAccountHead.getText().trim());
            builder.setString("principle", cmbPrinciple.getSelectedItem().toString());
            builder.set("status", rbActive.isSelected());

            if (database.insert(builder.insert("account_head")) > 0) {
                database.disconnect();
                return 1;
            } else {
                return -1;
            }

        } else {
            return -2;
        }

    }
